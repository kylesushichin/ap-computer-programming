public class Phrase
{
    public static void main(String args[])
    {
        System.out.println("Knowledge is power");
        System.out.println(" ");
        System.out.println("Knowledge"+"\n   is"+"\n  power");
        System.out.println(" ");
        System.out.println("|=====================|");
        System.out.println("|      Knowledge      |"+"\n|          is         |"+"\n|        power        |");
        System.out.println("|=====================|");
    }
}